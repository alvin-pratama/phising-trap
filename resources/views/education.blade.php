                        Baca informasi berikut ini dengan seksama ya, terkait edukasi tentang phishing trap.
                    </h2>
                    <p class="text-gray-800 font-medium mb-2">Apa itu Phishing?</p>
                    <p class="text-gray-600 mb-4">
                        Phishing adalah metode penipuan di mana pelaku menyamar sebagai pihak terpercaya untuk mencuri informasi sensitif,
                        seperti kata sandi dan data keuangan. Serangan ini sering dilakukan melalui email, pesan singkat, atau situs web palsu yang terlihat meyakinkan.
                    </p>

                    <p class="text-gray-800 font-medium mb-2">Ciri-ciri Phishing yang harus diwaspadai:</p>
                    <ul class="list-disc list-inside text-gray-600 space-y-2">
                        <li><span class="font-semibold">Alamat pengirim yang mencurigakan:</span> Periksa alamat email atau nomor pengirim. Jika terlihat aneh atau tidak resmi, waspadalah.</li>
                        <li><span class="font-semibold">Tautan yang tidak biasa:</span> Arahkan kursor ke tautan sebelum mengeklik. Jika berbeda dari yang terlihat, jangan klik!</li>
                        <li><span class="font-semibold">Permintaan data pribadi:</span> Instruksi resmi tidak akan meminta informasi sensitif melalui email atau pesan singkat.</li>
                        <li><span class="font-semibold">Nada mendesak atau ancaman:</span> Pesan phishing sering kali memaksa Anda untuk bertindak cepat, seperti ancaman penutupan akun.</li>
                        <li><span class="font-semibold">Tampilan situs yang mirip aslinya:</span> Selalu periksa URL dan sertifikat keamanan sebelum memasukkan data Anda.</li>
                    </ul>

                    <br>
                    <p class="text-gray-800 font-medium mb-2">Cara melindungi diri dari phising:</p>
                    <ul class="list-disc list-inside text-gray-600 space-y-2">
                        <li>Periksa URL dan Alamat pngirim sebelum mengklik tautan atau membalas pesan.</li>
                        <li>Gunakan Autentifikasi Dua Faktor (2FA) untuk menambah lapisan keamanan akun Anda.</li>
                        <li>Jangan mudah percaya dengan pesan mendesak yang meminta data pribadi atau keuangan.</li>
                        <li>Gunakan Antivirus dan Firewall untuk mendeteksi situs web berbahaya.</li>
                        <li>Laporkan upaya phising kepada pihak berwenang atau tim IT organisasi Anda.</li>
                    </ul>

